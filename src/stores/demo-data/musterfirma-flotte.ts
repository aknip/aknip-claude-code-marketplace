// Flottenstatistik
export interface FlottenStatistik {
  gesamtanzahl: number
  nachTyp: Record<Fahrzeugtyp, number>
  nachStatus: Record<Fahrzeugstatus, number>
  nachAntrieb: Record<string, number>
  gesamtpraemieHaftpflicht: number
  gesamtpraemieVollkasko: number
  gesamtpraemieTeilkasko: number
  durchschnittsalter: number
  durchschnittsKilometerstand: number
}

export function berechneFlottenStatistik(flotte: Fahrzeug[]): FlottenStatistik {
  const heute = new Date()

  const nachTyp = flotte.reduce(
    (acc, fzg) => {
      acc[fzg.fahrzeugtyp] = (acc[fzg.fahrzeugtyp] || 0) + 1
      return acc
    },
    {} as Record<Fahrzeugtyp, number>
  )

  const nachStatus = flotte.reduce(
    (acc, fzg) => {
      acc[fzg.status] = (acc[fzg.status] || 0) + 1
      return acc
    },
    {} as Record<Fahrzeugstatus, number>
  )

  const nachAntrieb = flotte.reduce(
    (acc, fzg) => {
      acc[fzg.antriebsart] = (acc[fzg.antriebsart] || 0) + 1
      return acc
    },
    {} as Record<string, number>
  )

  const gesamtpraemieHaftpflicht = flotte.reduce((sum, fzg) => sum + fzg.jahrespraemieHaftpflicht, 0)
  const gesamtpraemieVollkasko = flotte.reduce(
    (sum, fzg) => sum + (fzg.jahrespraemieVollkasko || 0),
    0
  )
  const gesamtpraemieTeilkasko = flotte.reduce(
    (sum, fzg) => sum + (fzg.jahrespraemieTeilkasko || 0),
    0
  )

  const fahrzeugeOhneAnhaenger = flotte.filter((fzg) => fzg.fahrzeugtyp !== 'Anhänger')
  const summeAlter = fahrzeugeOhneAnhaenger.reduce((sum, fzg) => {
    const erstzulassung = new Date(fzg.erstzulassung)
    const alterInMonaten =
      (heute.getFullYear() - erstzulassung.getFullYear()) * 12 +
      (heute.getMonth() - erstzulassung.getMonth())
    return sum + alterInMonaten / 12
  }, 0)
  const durchschnittsalter = summeAlter / fahrzeugeOhneAnhaenger.length

  const summeKm = fahrzeugeOhneAnhaenger.reduce((sum, fzg) => sum + fzg.kilometerstand, 0)
  const durchschnittsKilometerstand = summeKm / fahrzeugeOhneAnhaenger.length

  return {
    gesamtanzahl: flotte.length,
    nachTyp,
    nachStatus,
    nachAntrieb,
    gesamtpraemieHaftpflicht,
    gesamtpraemieVollkasko,
    gesamtpraemieTeilkasko,
    durchschnittsalter: Math.round(durchschnittsalter * 10) / 10,
    durchschnittsKilometerstand: Math.round(durchschnittsKilometerstand),
  }
}

// Helper Functions
export function getFahrzeugById(id: string): Fahrzeug | undefined {
  return musterfirmaFlotte.find((fzg) => fzg.id === id)
}

export function getFahrzeugeByTyp(typ: Fahrzeugtyp): Fahrzeug[] {
  return musterfirmaFlotte.filter((fzg) => fzg.fahrzeugtyp === typ)
}

export function getFahrzeugeByStatus(status: Fahrzeugstatus): Fahrzeug[] {
  return musterfirmaFlotte.filter((fzg) => fzg.status === status)
}

export function getAktiveFahrzeuge(): Fahrzeug[] {
  return musterfirmaFlotte.filter((fzg) => fzg.status === 'aktiv')
}

export function getFahrzeugeMitFaelligerHU(inTagen: number = 30): Fahrzeug[] {
  const heute = new Date()
  const grenze = new Date(heute.getTime() + inTagen * 24 * 60 * 60 * 1000)

  return musterfirmaFlotte.filter((fzg) => {
    const naechsteHU = new Date(fzg.naechsteHU)
    return naechsteHU <= grenze && fzg.status === 'aktiv'
  })
}

