  versicherungsbeginn: string
  jahrespraemieHaftpflicht: number
  jahrespraemieVollkasko?: number
  jahrespraemieTeilkasko?: number
  selbstbeteiligungVollkasko?: number
  selbstbeteiligungTeilkasko?: number
  fahrer?: string
  abteilung?: string
  standort: string
