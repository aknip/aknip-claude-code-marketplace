// Statistik-Funktionen
export interface SchadenStatistik {
  gesamtanzahl: number
  nachJahr: Record<number, number>
  nachTyp: Record<SchadenTyp, number>
  nachUrsache: Record<SchadenUrsache, number>
  nachStatus: Record<SchadenStatus, number>
  gesamtschadenhoehe: number
  gesamtauszahlung: number
  durchschnittsschaden: number
  schadenquote: number // Schäden pro Jahr
  personenschaeden: number
}

export function berechneSchadenStatistik(schaeden: Schaden[]): SchadenStatistik {
  const nachJahr = schaeden.reduce(
    (acc, s) => {
      const jahr = parseInt(s.schadendatum.substring(0, 4))
      acc[jahr] = (acc[jahr] || 0) + 1
      return acc
    },
    {} as Record<number, number>
  )

  const nachTyp = schaeden.reduce(
    (acc, s) => {
      acc[s.schadenTyp] = (acc[s.schadenTyp] || 0) + 1
      return acc
    },
    {} as Record<SchadenTyp, number>
  )

  const nachUrsache = schaeden.reduce(
    (acc, s) => {
      acc[s.schadenUrsache] = (acc[s.schadenUrsache] || 0) + 1
      return acc
    },
    {} as Record<SchadenUrsache, number>
  )

  const nachStatus = schaeden.reduce(
    (acc, s) => {
      acc[s.status] = (acc[s.status] || 0) + 1
      return acc
    },
    {} as Record<SchadenStatus, number>
  )

  const gesamtschadenhoehe = schaeden.reduce((sum, s) => sum + s.schadenhoehe, 0)
  const gesamtauszahlung = schaeden.reduce((sum, s) => sum + s.auszahlung, 0)
  const personenschaeden = schaeden.filter((s) => s.personenschaden).length

  const jahre = Object.keys(nachJahr).length
  const schadenquote = schaeden.length / jahre

  return {
    gesamtanzahl: schaeden.length,
    nachJahr,
    nachTyp,
    nachUrsache,
    nachStatus,
    gesamtschadenhoehe,
    gesamtauszahlung,
    durchschnittsschaden: Math.round(gesamtschadenhoehe / schaeden.length),
    schadenquote: Math.round(schadenquote * 10) / 10,
    personenschaeden,
  }
}

